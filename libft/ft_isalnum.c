#include "libft.h"

int	ft_isalnum(int c)
{
	if (c < '0' || '9' < c)
		return (1);
	else if (c < 'a' || 'z' < c)
		return (1);
	else if (c < 'A' || 'Z' < c)
		return (1);
	else
		return (0);
}
